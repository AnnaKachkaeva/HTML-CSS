x = 0.5
s = 0

n = 8
h = 4.1/n

print("n=" + str(n) + "   h=" + str(h))

for i in range(n+1):
    f = (x + 1.8)/(x*x + x + 2.8)
    print("f(" + str(x) + ")=" + str(f))
    x += h
    if (i!=0 & i!=n+1):
        if i%2==0:
            s += 4*f
        if i%2==1:
            s += 2*f    
        
    else:
        s += f

print("I=" + str((s*(h/3))))


